    private void consultarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarBActionPerformed
        
         Boolean aux=false;
                  
         
            if(dueñoTxt.getText().equals("")){
            JOptionPane.showMessageDialog(null,"campo de dueño vacio!!");
            }
            else
            {       
              for(int i=0;i<=vehiculoList.size();i++){
                           
                if(personaList.get(i).equals(dueñoTxt.getText())){
                    aux=true;                                     
                 }
                  if(aux=true){ 
                     areaTxt.setText(vehiculoList.get(i).toString()+personaList.get(i).toString()+"\nEsta persona tiene "+contador+" vehiculos.");
                     //areaTxt.setText("Usuario registrado.");                     
                 }
                    else{
                         areaTxt.setText("\nEl Usuario no esta registrado.");
                    }
                }
            }
        
    }//GEN-LAST:event_consultarBActionPerformed

