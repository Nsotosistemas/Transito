    private void guardarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBActionPerformed
       
        Vehiculo v = new Vehiculo();
        
        v.setPlaca(placaTxt.getText());
        v.setModelo(Integer.parseInt(modeloTxt.getText()));
        v.setMarca(marcaTxt.getText());
        v.setDueño(dueñoTxt.getText()); 
        
        vehiculoList.add(v);
               
        Persona p = new Persona();
        
        p.setCedula(cedulaTxt.getText());
        p.setNombre(nameTxt.getText());
        p.setApellido(apellidoTxt.getText());
        p.setContacto(contactoTxt.getText());
        
        personaList.add(p);
        
        for(int i=0;i<=personaList.size();i++){
         
            if(personaList.get(i).equals(dueñoTxt.getText())){
            contador=contador+1;
            }
        } 
         
    
        JOptionPane.showMessageDialog(null,"Vehiculo y propietario almacenado");
        limpiar();
    }//GEN-LAST:event_guardarBActionPerformed

